package invaid.users.model;

public class AccountSettingDetail {
	private String user_firstName;
	private String user_lastName;
	private String user_birthday;
	private String user_email;
	private String user_telno;
	private String user_cellno;
	private String user_occupation;
	private String user_company;
	
	public String getUser_firstName() {
		return user_firstName;
	}
	
	public void setUser_firstName(String user_firstName) {
		this.user_firstName = user_firstName;
	}
	
	public String getUser_lastName() {
		return user_lastName;
	}
	
	public void setUser_lastName(String user_lastName) {
		this.user_lastName = user_lastName;
	}
	
	public String getUser_birthday() {
		return user_birthday;
	}
	
	public void setUser_birthday(String user_birthday) {
		this.user_birthday = user_birthday;
	}
	
	public String getUser_email() {
		return user_email;
	}
	
	public void setUser_email(String user_email) {
		this.user_email = user_email;
	}
	
	public String getUser_telno() {
		return user_telno;
	}
	
	public void setUser_telno(String user_telno) {
		this.user_telno = user_telno;
	}
	
	public String getUser_cellno() {
		return user_cellno;
	}
	
	public void setUser_cellno(String user_cellno) {
		this.user_cellno = user_cellno;
	}
	
	public String getUser_occupation() {
		return user_occupation;
	}
	
	public void setUser_occupation(String user_occupation) {
		this.user_occupation = user_occupation;
	}
	
	public String getUser_company() {
		return user_company;
	}
	
	public void setUser_company(String user_company) {
		this.user_company = user_company;
	}
	
	

}
