package invaid.users.model;

public class PortfolioModel {
	private String overallGainLoss;
	private String overallGainLossPct;
		
	public String getOverallGainLoss() {
		return overallGainLoss;
	}
	
	public void setOverallGainLoss(String overallGainLoss) {
		this.overallGainLoss = overallGainLoss;
	}
	
	public String getOverallGainLossPct() {
		return overallGainLossPct;
	}
	
	public void setOverallGainLossPct(String overallGainLossPct) {
		this.overallGainLossPct = overallGainLossPct;
	}
	
	
}
